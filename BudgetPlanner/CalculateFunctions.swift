import Foundation
import UIKit



final class CalculateFunctions {
        
    static func plus(_ firstValue: Double, _ secondValue: Double) -> Double {
        return firstValue + secondValue
    }
    static func minus(_ firstValue: Double, _ secondValue: Double) -> Double {
        return firstValue - secondValue
    }
    static func devide(_ firstValue: Double, _ secondValue: Double) -> Double {
        return firstValue / secondValue
    }
    static func multiply(_ firstValue: Double, _ secondValue: Double) -> Double {
        return firstValue * secondValue
    }
    
}
